// Conditionals
// Booleans : true or false

let num = 2;

if (num === 4) {
  console.log("num is equal to four");
} else if (num === 5) {
  console.log("num is equal to five");
} else if (num === 3) {
  console.log("num is equal to three");
} else {
  console.log("num is equal to two!");
}

